# be overwritten the next time that script is run. You should just edit the script to be correct.

eos

#Ignore Everything
puts "*"

# Except This Stuff
puts "!.gitignore"
directories_in_the_common_files.each do |dir|
  puts negated_ignore_lines_for_a_directory(dir)
end

